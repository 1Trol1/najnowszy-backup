/* Prosty renderer pod Twój framebuffer */
static inline void draw_char_8x16(
    uint32_t *fb, int fb_width,
    int x, int y,
    char c,
    uint32_t fg, uint32_t bg
) {
    const uint8_t *glyph = font8x16[(unsigned char)c];
    for (int row = 0; row < 16; ++row) {
        uint8_t bits = glyph[row];
        uint32_t *dst = fb + (y + row) * fb_width + x;
        for (int col = 0; col < 8; ++col) {
            uint32_t color = (bits & (0x80 >> col)) ? fg : bg;
            dst[col] = color;
        }
    }
}

static inline void draw_string_8x16(
    uint32_t *fb, int fb_width,
    int x, int y,
    const char *s,
    uint32_t fg, uint32_t bg
) {
    int cx = x;
    while (*s) {
        if (*s == '\n') {
            cx = x;
            y += 16;
            s++;
            continue;
        }
        draw_char_8x16(fb, fb_width, cx, y, *s, fg, bg);
        cx += 8;
        s++;
    }
}
